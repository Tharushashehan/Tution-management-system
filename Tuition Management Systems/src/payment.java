    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed

        if(txt_index_number.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "Fill the Index Number");
        }
        else if(jcombo_grade.getSelectedItem().equals("--Select--")){

            JOptionPane.showMessageDialog(null, "Select the Grade");

        }
        else if(jcombo_subject.getSelectedItem().equals("---Select---")){

            JOptionPane.showMessageDialog(null, "Select the Subject");

        }
        else if(txt_amount.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "Enter the Amount");

        }

        else if(txt_date.getDate()==null){

            JOptionPane.showMessageDialog(null, "Select the Date");

        }
        else
        {

            try {

                DateFormat y_m_d= new SimpleDateFormat("yyyy-MM-dd");
                String sql="INSERT INTO payment(student_index,grade,subject,amount,date) VALUES(?,?,?,?,?)";
                pst=conn.prepareStatement(sql);
                pst.setString(1, txt_index_number.getText());
                pst.setString(2, jcombo_grade.getSelectedItem().toString());
                pst.setString(3, jcombo_subject.getSelectedItem().toString());
                pst.setString(4,txt_amount.getText());
                pst.setString(5, y_m_d.format(txt_date.getDate()));
                pst.execute();
                JOptionPane.showMessageDialog(null, "Saved!!!");
                clean();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }
        Update_table();
        Update_Search_table();
    }//GEN-LAST:event_btn_addActionPerformed

