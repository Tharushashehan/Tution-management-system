    private void payment_detailsKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_payment_detailsKeyReleased
        // This is key released event

        if((evt.getKeyCode()==KeyEvent.VK_UP)||(evt.getKeyCode()==KeyEvent.VK_DOWN)){

            try {

                int row=payment_details.getSelectedRow();
                String table_click=(payment_details.getModel().getValueAt(row,0).toString());
                String sql="SELECT student_index,grade,subject,amount,date FROM payment WHERE student_index='"+table_click+"'";
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                if(rs.next()){

                    String add=rs.getString("student_index");
                    txt_index_number.setText(add);
                    String add1=rs.getString("grade");
                    jcombo_grade.setSelectedItem(add1);
                    String add2=rs.getString("subject");
                    jcombo_subject.setSelectedItem(add2);
                    String add3=rs.getString("amount");
                    txt_amount.setText(add3);
                    Date add4=rs.getDate("date");
                    txt_date.setDate(add4);

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_payment_detailsKeyReleased

